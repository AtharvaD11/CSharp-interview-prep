What is CLR? (Common Language Runtime)

 CLR is the execution engine that runs our .NET apps.
When we compile our C# code, it directly doesn't get compiled into machine code.
Instead, it is compiled into an IL. The CLR then takes this IL and using a JIT compiler, converts it into a native machine code.
The CPU then executes this machine code.

Apart from code execution, it provides set of services like:
* memory management and garbage collection
* exception handling
* type safety
* networking
* security
* thread management


